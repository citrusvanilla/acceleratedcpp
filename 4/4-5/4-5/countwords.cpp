#include <vector>
#include <string>
#include "countwords.h"

using std::vector;
using std::string;

vector<string>::size_type countwords(vector<string> vec) {
    typedef vector<string>::size_type vec_sz;
    vec_sz count = vec.size();
    return count;
}


